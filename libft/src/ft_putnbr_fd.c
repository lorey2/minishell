#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	char	c;
	long	n;

	n = (long)nb;
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = n * -1;
	}
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	c = (n % 10) + '0';
	ft_putchar_fd(c, fd);
}
